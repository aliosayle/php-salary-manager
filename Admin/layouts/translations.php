/**
 * Get a translated string based on the current language
 * 
 * @param string $key The translation key
 * @param string|null $lang The language to use (default: current session language or 'en')
 * @return string The translated string or the key if translation not found
 */
function __($key, $lang = null) {
    global $translations;
    
    // Determine language
    if (!$lang) {
        $lang = isset($_SESSION['lang']) ? $_SESSION['lang'] : 'en';
    }
    
    // Ensure we have a valid language
    if (!isset($translations[$lang])) {
        $lang = 'en'; // Fallback to English
    }
    
    // Return the translation or the key if not found
    return isset($translations[$lang][$key]) ? $translations[$lang][$key] : $key;
}

/**
 * Check if a translation key exists
 * 
 * @param string $key The translation key
 * @param string|null $lang The language to check (default: current session language or 'en')
 * @return bool True if the key exists in the language, false otherwise
 */
function has_translation($key, $lang = null) {
    global $translations;
    
    // Determine language
    if (!$lang) {
        $lang = isset($_SESSION['lang']) ? $_SESSION['lang'] : 'en';
    }
    
    // Ensure we have a valid language
    if (!isset($translations[$lang])) {
        $lang = 'en'; // Fallback to English
    }
    
    return isset($translations[$lang][$key]);
}

